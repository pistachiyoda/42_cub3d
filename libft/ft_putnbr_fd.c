#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	i = 0;
	if (n / 10)
	    ft_putnbr_fd(n / 10, 1);
    ft_putchar_fd(n % 10 + '0', fd);
}
